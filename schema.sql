BEGIN;

DROP VIEW IF EXISTS agent_performance_view;
DROP VIEW IF EXISTS active_orders_view;
DROP TABLE IF EXISTS order_security_audit, order_assignment_audit, inventory_audit, pricing_rule_audit, agent_location_history, agent_location,
  inventory_reservations, gift_delivery_details, order_charge_lines, order_items,
  order_status_log, orders, delivery_time_slots, delivery_quote_lines,
  delivery_quote_items, delivery_quotes, coupons, pricing_rules, cart_items, carts,
  addresses, agent_zones, agents, vehicle_types, product_variants, products,
  categories, zone_routes, zones, users CASCADE;

CREATE TABLE users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  email VARCHAR(180) NOT NULL UNIQUE,
  phone VARCHAR(20),
  password_hash VARCHAR(130) NOT NULL,
  role VARCHAR(20) NOT NULL CHECK (role IN ('CUSTOMER', 'AGENT', 'ADMIN')),
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE zones (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80) NOT NULL UNIQUE,
  center_lat DECIMAL(9,6) NOT NULL CHECK (center_lat BETWEEN -90 AND 90),
  center_lng DECIMAL(9,6) NOT NULL CHECK (center_lng BETWEEN -180 AND 180)
);

CREATE TABLE zone_routes (
  from_zone_id BIGINT NOT NULL REFERENCES zones(id) ON DELETE CASCADE,
  to_zone_id BIGINT NOT NULL REFERENCES zones(id) ON DELETE CASCADE,
  distance DECIMAL(8,2) NOT NULL CHECK (distance > 0),
  PRIMARY KEY (from_zone_id, to_zone_id),
  CHECK (from_zone_id <> to_zone_id)
);

CREATE TABLE categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80) NOT NULL UNIQUE,
  description VARCHAR(300) NOT NULL,
  handling_type VARCHAR(30) NOT NULL,
  fragile_default BOOLEAN NOT NULL DEFAULT FALSE,
  temperature_controlled_default BOOLEAN NOT NULL DEFAULT FALSE,
  requires_insurance_default BOOLEAN NOT NULL DEFAULT FALSE,
  max_quantity INTEGER NOT NULL DEFAULT 10 CHECK (max_quantity > 0),
  max_delivery_distance_km DECIMAL(8,2) NOT NULL DEFAULT 25 CHECK (max_delivery_distance_km > 0),
  delivery_restrictions VARCHAR(300),
  return_eligible BOOLEAN NOT NULL DEFAULT TRUE,
  default_priority VARCHAR(12) NOT NULL DEFAULT 'STANDARD'
    CHECK (default_priority IN ('STANDARD','EXPRESS','CRITICAL')),
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category_id BIGINT NOT NULL REFERENCES categories(id),
  name VARCHAR(120) NOT NULL,
  description VARCHAR(700) NOT NULL,
  sku VARCHAR(60) NOT NULL UNIQUE,
  pricing_type VARCHAR(20) NOT NULL CHECK (pricing_type IN ('FIXED','WEIGHT','VARIANT')),
  unit_price DECIMAL(12,2),
  price_per_kg DECIMAL(12,2),
  unit_weight_kg DECIMAL(10,3),
  minimum_order_weight DECIMAL(10,3),
  maximum_order_weight DECIMAL(10,3),
  packaging_weight_kg DECIMAL(10,3) NOT NULL DEFAULT 0 CHECK (packaging_weight_kg >= 0),
  stock_quantity DECIMAL(12,3) NOT NULL CHECK (stock_quantity >= 0),
  fragile BOOLEAN NOT NULL DEFAULT FALSE,
  temperature_controlled BOOLEAN NOT NULL DEFAULT FALSE,
  requires_insurance BOOLEAN NOT NULL DEFAULT FALSE,
  shelf_life_days INTEGER CHECK (shelf_life_days IS NULL OR shelf_life_days > 0),
  image_url VARCHAR(500),
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CHECK (minimum_order_weight IS NULL OR maximum_order_weight IS NULL OR minimum_order_weight <= maximum_order_weight),
  CHECK ((pricing_type='FIXED' AND unit_price IS NOT NULL AND unit_price >= 0 AND unit_weight_kg > 0)
      OR (pricing_type='WEIGHT' AND price_per_kg IS NOT NULL AND price_per_kg >= 0
          AND minimum_order_weight > 0 AND maximum_order_weight >= minimum_order_weight)
      OR pricing_type='VARIANT')
);

CREATE TABLE product_variants (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL REFERENCES products(id) ON DELETE CASCADE,
  label VARCHAR(80) NOT NULL,
  sku VARCHAR(60) NOT NULL UNIQUE,
  unit_price DECIMAL(12,2),
  price_per_kg DECIMAL(12,2),
  unit_weight_kg DECIMAL(10,3) NOT NULL CHECK (unit_weight_kg > 0),
  minimum_order_weight DECIMAL(10,3),
  maximum_order_weight DECIMAL(10,3),
  stock_quantity DECIMAL(12,3) NOT NULL CHECK (stock_quantity >= 0),
  delivery_restriction VARCHAR(300),
  active BOOLEAN NOT NULL DEFAULT TRUE,
  UNIQUE (product_id, label),
  CHECK (unit_price IS NOT NULL OR price_per_kg IS NOT NULL)
);

CREATE INDEX idx_products_category_active ON products(category_id, active);
CREATE INDEX idx_variants_product_active ON product_variants(product_id, active);

CREATE TABLE vehicle_types (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(30) NOT NULL UNIQUE,
  maximum_capacity_kg DECIMAL(10,2) NOT NULL CHECK (maximum_capacity_kg > 0),
  maximum_active_orders INTEGER NOT NULL CHECK (maximum_active_orders > 0),
  supports_fragile BOOLEAN NOT NULL DEFAULT FALSE,
  supports_temperature_control BOOLEAN NOT NULL DEFAULT FALSE,
  handling_equipment VARCHAR(200)
);

CREATE TABLE agents (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
  vehicle_type_id BIGINT NOT NULL REFERENCES vehicle_types(id),
  current_lat DECIMAL(9,6) NOT NULL CHECK (current_lat BETWEEN -90 AND 90),
  current_lng DECIMAL(9,6) NOT NULL CHECK (current_lng BETWEEN -180 AND 180),
  status VARCHAR(20) NOT NULL DEFAULT 'AVAILABLE'
    CHECK (status IN ('AVAILABLE','BUSY','OFFLINE')),
  current_load_kg DECIMAL(10,3) NOT NULL DEFAULT 0 CHECK (current_load_kg >= 0),
  current_active_orders INTEGER NOT NULL DEFAULT 0 CHECK (current_active_orders >= 0),
  rating DECIMAL(3,2) NOT NULL DEFAULT 5 CHECK (rating BETWEEN 0 AND 5)
);

CREATE TABLE agent_zones (
  agent_id BIGINT NOT NULL REFERENCES agents(id) ON DELETE CASCADE,
  zone_id BIGINT NOT NULL REFERENCES zones(id) ON DELETE CASCADE,
  PRIMARY KEY (agent_id, zone_id)
);

CREATE INDEX idx_agents_status_capacity ON agents(status, current_active_orders, current_load_kg);

CREATE TABLE addresses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  label VARCHAR(20) NOT NULL CHECK (label IN ('HOME','WORK','OTHER')),
  address_line VARCHAR(240) NOT NULL,
  landmark VARCHAR(120),
  instructions VARCHAR(300),
  latitude DECIMAL(9,6) NOT NULL CHECK (latitude BETWEEN -90 AND 90),
  longitude DECIMAL(9,6) NOT NULL CHECK (longitude BETWEEN -180 AND 180),
  zone_id BIGINT NOT NULL REFERENCES zones(id),
  is_default BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_addresses_customer ON addresses(customer_id, is_default DESC);

CREATE TABLE carts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE cart_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cart_id BIGINT NOT NULL REFERENCES carts(id) ON DELETE CASCADE,
  product_id BIGINT NOT NULL REFERENCES products(id),
  variant_id BIGINT REFERENCES product_variants(id),
  quantity INTEGER NOT NULL DEFAULT 1 CHECK (quantity > 0),
  selected_weight_kg DECIMAL(10,3) CHECK (selected_weight_kg IS NULL OR selected_weight_kg > 0),
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE NULLS NOT DISTINCT (cart_id, product_id, variant_id)
);

CREATE TABLE pricing_rules (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  rule_type VARCHAR(40) NOT NULL CHECK (rule_type IN ('BASE_DELIVERY','DISTANCE','WEIGHT',
    'CATEGORY_HANDLING','CATEGORY_PACKAGING','FRAGILE','TEMPERATURE_CONTROL','INSURANCE',
    'PRIORITY','GIFT_WRAP','SCHEDULED_DELIVERY','PLATFORM_FEE','TAX')),
  category_id BIGINT REFERENCES categories(id) ON DELETE CASCADE,
  applies_to VARCHAR(40),
  minimum_value DECIMAL(12,3),
  maximum_value DECIMAL(12,3),
  flat_amount DECIMAL(12,2) NOT NULL DEFAULT 0 CHECK (flat_amount >= 0),
  per_unit_amount DECIMAL(12,2) NOT NULL DEFAULT 0 CHECK (per_unit_amount >= 0),
  percentage_amount DECIMAL(8,4) NOT NULL DEFAULT 0 CHECK (percentage_amount >= 0),
  priority INTEGER NOT NULL DEFAULT 0,
  effective_from TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  effective_until TIMESTAMP WITH TIME ZONE,
  active BOOLEAN NOT NULL DEFAULT TRUE,
  CHECK (minimum_value IS NULL OR maximum_value IS NULL OR minimum_value < maximum_value)
);
CREATE INDEX idx_pricing_rules_type_active ON pricing_rules(rule_type, active);

CREATE TABLE coupons (
  code VARCHAR(30) PRIMARY KEY,
  percentage_amount DECIMAL(8,4) NOT NULL CHECK (percentage_amount > 0),
  maximum_discount DECIMAL(12,2) NOT NULL CHECK (maximum_discount > 0),
  minimum_order_value DECIMAL(12,2) NOT NULL DEFAULT 0,
  effective_from TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  effective_until TIMESTAMP WITH TIME ZONE NOT NULL,
  active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE delivery_quotes (
  id UUID PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  currency CHAR(3) NOT NULL DEFAULT 'INR',
  request_hash CHAR(64) NOT NULL,
  request_payload JSONB NOT NULL,
  product_subtotal DECIMAL(12,2) NOT NULL,
  base_delivery_fee DECIMAL(12,2) NOT NULL,
  distance_km DECIMAL(10,2) NOT NULL CHECK (distance_km >= 0),
  distance_charge DECIMAL(12,2) NOT NULL,
  total_weight_kg DECIMAL(10,3) NOT NULL CHECK (total_weight_kg > 0),
  weight_charge DECIMAL(12,2) NOT NULL,
  category_charge DECIMAL(12,2) NOT NULL,
  special_handling_charge DECIMAL(12,2) NOT NULL,
  gift_charge DECIMAL(12,2) NOT NULL,
  scheduling_charge DECIMAL(12,2) NOT NULL,
  platform_fee DECIMAL(12,2) NOT NULL,
  tax_total DECIMAL(12,2) NOT NULL,
  discount_total DECIMAL(12,2) NOT NULL,
  delivery_total DECIMAL(12,2) NOT NULL,
  final_total DECIMAL(12,2) NOT NULL,
  expires_at TIMESTAMP WITH TIME ZONE NOT NULL,
  used_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_delivery_quotes_customer_expiry ON delivery_quotes(customer_id, expires_at);

CREATE TABLE delivery_quote_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  quote_id UUID NOT NULL REFERENCES delivery_quotes(id) ON DELETE CASCADE,
  product_id BIGINT NOT NULL REFERENCES products(id),
  variant_id BIGINT REFERENCES product_variants(id),
  product_name VARCHAR(120) NOT NULL,
  variant_label VARCHAR(80),
  quantity INTEGER NOT NULL CHECK (quantity > 0),
  selected_weight_kg DECIMAL(10,3),
  stock_demand DECIMAL(12,3) NOT NULL CHECK (stock_demand > 0),
  unit_price_snapshot DECIMAL(12,2),
  price_per_kg_snapshot DECIMAL(12,2),
  unit_weight_snapshot DECIMAL(10,3) NOT NULL,
  line_subtotal DECIMAL(12,2) NOT NULL,
  fragile BOOLEAN NOT NULL,
  temperature_controlled BOOLEAN NOT NULL,
  requires_insurance BOOLEAN NOT NULL
);

CREATE TABLE delivery_quote_lines (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  quote_id UUID NOT NULL REFERENCES delivery_quotes(id) ON DELETE CASCADE,
  charge_type VARCHAR(40) NOT NULL,
  label VARCHAR(120) NOT NULL,
  amount DECIMAL(12,2) NOT NULL,
  sort_order INTEGER NOT NULL
);

CREATE TABLE delivery_time_slots (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80) NOT NULL,
  day_of_week INTEGER NOT NULL CHECK (day_of_week BETWEEN 0 AND 6),
  start_time TIME NOT NULL,
  end_time TIME NOT NULL,
  capacity INTEGER NOT NULL CHECK (capacity > 0),
  preparation_minutes INTEGER NOT NULL DEFAULT 120 CHECK (preparation_minutes >= 0),
  active BOOLEAN NOT NULL DEFAULT TRUE,
  UNIQUE (day_of_week, start_time, end_time),
  CHECK (start_time < end_time)
);

CREATE TABLE orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES users(id),
  quote_id UUID UNIQUE REFERENCES delivery_quotes(id),
  idempotency_key VARCHAR(100),
  agent_id BIGINT REFERENCES agents(id) ON DELETE SET NULL,
  pickup_address VARCHAR(240) NOT NULL,
  drop_address VARCHAR(240) NOT NULL,
  pickup_lat DECIMAL(9,6) NOT NULL CHECK (pickup_lat BETWEEN -90 AND 90),
  pickup_lng DECIMAL(9,6) NOT NULL CHECK (pickup_lng BETWEEN -180 AND 180),
  drop_lat DECIMAL(9,6) NOT NULL CHECK (drop_lat BETWEEN -90 AND 90),
  drop_lng DECIMAL(9,6) NOT NULL CHECK (drop_lng BETWEEN -180 AND 180),
  pickup_zone_id BIGINT NOT NULL REFERENCES zones(id),
  drop_zone_id BIGINT NOT NULL REFERENCES zones(id),
  package_name VARCHAR(160) NOT NULL,
  item_count INTEGER NOT NULL DEFAULT 1 CHECK (item_count > 0),
  total_weight_kg DECIMAL(10,3) NOT NULL CHECK (total_weight_kg > 0),
  notes VARCHAR(500),
  status VARCHAR(24) NOT NULL CHECK (status IN
    ('SCHEDULED','PLACED','CONFIRMED','PACKED','ASSIGNED','PICKED_UP','OUT_FOR_DELIVERY','DELIVERY_VERIFICATION','DELIVERED','CANCELLED')),
  order_type VARCHAR(12) NOT NULL DEFAULT 'NORMAL' CHECK (order_type IN ('NORMAL','GIFT')),
  priority VARCHAR(12) NOT NULL DEFAULT 'STANDARD'
    CHECK (priority IN ('STANDARD','EXPRESS','CRITICAL')),
  fragile BOOLEAN NOT NULL DEFAULT FALSE,
  temperature_controlled BOOLEAN NOT NULL DEFAULT FALSE,
  price DECIMAL(12,2) NOT NULL CHECK (price >= 0),
  scheduled_delivery_at TIMESTAMP WITH TIME ZONE,
  assignment_release_at TIMESTAMP WITH TIME ZONE,
  delivery_window_start TIMESTAMP WITH TIME ZONE,
  delivery_window_end TIMESTAMP WITH TIME ZONE,
  timezone VARCHAR(50) NOT NULL DEFAULT 'Asia/Kolkata',
  version INTEGER NOT NULL DEFAULT 0 CHECK (version >= 0),
  delivery_code_hash VARCHAR(180),
  delivery_code_ciphertext VARCHAR(200),
  delivery_code_fingerprint CHAR(64),
  delivery_code_expires_at TIMESTAMP WITH TIME ZONE,
  delivery_code_verified_at TIMESTAMP WITH TIME ZONE,
  delivery_code_verified_by BIGINT REFERENCES users(id),
  delivery_code_verification_role VARCHAR(10) CHECK (delivery_code_verification_role IN ('AGENT','ADMIN')),
  delivery_code_failed_attempts INTEGER NOT NULL DEFAULT 0 CHECK (delivery_code_failed_attempts BETWEEN 0 AND 5),
  delivery_code_blocked_until TIMESTAMP WITH TIME ZONE,
  delivery_partner_id BIGINT REFERENCES agents(id),
  manual_delivery_confirmation_reason VARCHAR(500),
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  delivered_at TIMESTAMP WITH TIME ZONE,
  CHECK ((status='SCHEDULED' AND scheduled_delivery_at IS NOT NULL AND assignment_release_at IS NOT NULL)
      OR status<>'SCHEDULED'),
  CHECK ((status='DELIVERED' AND delivered_at IS NOT NULL)
      OR (status<>'DELIVERED' AND delivered_at IS NULL))
);
CREATE INDEX idx_orders_status_schedule ON orders(status, assignment_release_at);
CREATE INDEX idx_orders_customer_created ON orders(customer_id, created_at DESC);
CREATE INDEX idx_orders_agent_status ON orders(agent_id, status);
CREATE INDEX idx_orders_pending_fifo ON orders(created_at, id)
  WHERE status='PACKED' AND agent_id IS NULL;
CREATE UNIQUE INDEX idx_orders_delivery_code_fingerprint ON orders(delivery_code_fingerprint)
  WHERE delivery_code_fingerprint IS NOT NULL;
CREATE UNIQUE INDEX idx_orders_customer_idempotency ON orders(customer_id, idempotency_key)
  WHERE idempotency_key IS NOT NULL;

